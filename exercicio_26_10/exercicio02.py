# atividade receber 2 valores um texto e um inteiro e mostrar no final

caractere = input("Digite uma palavra: ")

inteiro = int(input("Digite um número inteiro: "))

print("A variável é do tipo: ", type(caractere))
print("O texto digitado foi: ", caractere)
print("A variável é do tipo: ", type(inteiro))
print("O número digitado foi: ", inteiro)

real = float(inteiro)
print("O número digitado foi: ", real)
print("A variável é do tipo: ", type(real))





